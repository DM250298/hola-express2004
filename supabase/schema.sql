-- Política genérica: authenticated puede todo. Reemplazar por políticas
-- por rol al endurecer producción.
do $$
declare
  tabla text;
begin
  for tabla in
    select unnest(array[
      'usuarios', 'categorias', 'proveedores', 'productos',
      'caja_turnos', 'ventas', 'items_venta', 'lotes',
      'movimientos_stock', 'pedidos', 'items_pedido',
      'egresos', 'cuentas_a_pagar'
    ])
  loop
    execute format(
      'create policy "authenticated todo" on public.%I for all to authenticated using (true) with check (true)',
      tabla
    );
  end loop;
end;
$$;

