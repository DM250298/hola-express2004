-- ─── Tabla: usuarios ────────────────────────────────────────────────

create table public.usuarios (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  nombre text not null,
  rol public.rol not null default 'cajero',
  activo boolean not null default true,
  created_at timestamptz not null default now()
);

-- ─── Tabla: categorias ──────────────────────────────────────────────

create table public.categorias (
  id integer generated by default as identity primary key,
  nombre text not null unique,
  descripcion text,
  created_at timestamptz not null default now()
);

-- ─── Tabla: proveedores ─────────────────────────────────────────────

create table public.proveedores (
  id integer generated by default as identity primary key,
  nombre text not null,
  telefono text,
  email text,
  dias_entrega integer check (dias_entrega is null or dias_entrega >= 0),
  condicion_pago text,
  created_at timestamptz not null default now()
);

-- ─── Tabla: productos ───────────────────────────────────────────────

create table public.productos (
  id integer generated by default as identity primary key,
  codigo_barras text unique,
  nombre text not null,
  categoria_id integer references public.categorias(id) on delete set null,
  proveedor_id integer references public.proveedores(id) on delete set null,
  precio_venta numeric(12, 2) not null check (precio_venta >= 0),
  precio_costo numeric(12, 2) not null default 0 check (precio_costo >= 0),
  stock_actual integer not null default 0,
  stock_minimo integer not null default 5 check (stock_minimo >= 0),
  activo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index productos_categoria_idx on public.productos(categoria_id);
create index productos_proveedor_idx on public.productos(proveedor_id);
create index productos_activo_idx on public.productos(activo) where activo = true;

