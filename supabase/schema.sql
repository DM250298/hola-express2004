-- ─── Trigger: updated_at automático en productos y pedidos ──────────

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger productos_set_updated_at
  before update on public.productos
  for each row execute procedure public.set_updated_at();

create trigger pedidos_set_updated_at
  before update on public.pedidos
  for each row execute procedure public.set_updated_at();

-- ─── Trigger: al crear auth.users, crear fila en public.usuarios ───
-- El primer usuario que se cree queda como 'cajero'. El admin lo promueve
-- después editando la fila desde Supabase Studio (o desde la app).

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.usuarios (id, email, nombre, rol, activo)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'nombre', split_part(new.email, '@', 1)),
    'cajero',
    true
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

